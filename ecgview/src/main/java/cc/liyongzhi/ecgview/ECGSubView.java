public class ECGSubView {

    private int offsetStartPoint = 0;
    private int subHeight = 0;
    private int subWidth = 0;
    private int width = 0;
    private int height = 0;

    public ECGSubView(int offsetStartPoint, int subHeight, int subWidth, int width, int height) {
        this.offsetStartPoint = offsetStartPoint;
        this.subHeight = subHeight;
        this.subWidth = subWidth;
        this.width = width;
        this.height = height;
    }




}
